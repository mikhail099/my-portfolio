let a = 10, b = 5;

console.log(a + b);  // Addition: 15
console.log(a - b);  // Subtraction: 5
console.log(a * b);  // Multiplication: 50
console.log(a / b);  // Division: 2
console.log(a % b);  // Modulus (remainder): 0
console.log(a ** b); // Exponentiation: 100000


console.log(Math.PI);        // 3.141592653589793
console.log(Math.E);         // Euler's number: 2.718
console.log(Math.SQRT2);     // Square root of 2


console.log(Math.round(4.7));  // 5  (Round to the nearest integer)
console.log(Math.floor(4.7));  // 4  (Round down)
console.log(Math.ceil(4.3));   // 5  (Round up)
console.log(Math.trunc(4.7));  // 4  (Remove decimal part)


console.log(Math.random());  // Random number between 0 and 1
console.log(Math.floor(Math.random() * 10) + 1); // Random number between 1 and 10


console.log(Math.pow(2, 3));  // 8 (2^3)
console.log(Math.sqrt(16));   // 4 (Square root of 16)
console.log(Math.cbrt(27));   // 3 (Cube root of 27)


console.log(Math.sin(Math.PI / 2)); // 1 (sin 90 degrees)
console.log(Math.cos(0));           // 1 (cos 0 degrees)
console.log(Math.tan(Math.PI / 4)); // 1 (tan 45 degrees)


console.log(Math.log(10));     // Natural log of 10
console.log(Math.log2(8));     // Log base 2 of 8 (result: 3)
console.log(Math.log10(100));  // Log base 10 of 100 (result: 2)
